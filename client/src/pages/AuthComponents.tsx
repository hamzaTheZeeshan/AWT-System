interface SocialButtonsProps {
  onFacebook?: () => void;
  onApple?: () => void;
  onGoogle?: () => void;
}

export function SocialButtons({ onFacebook, onApple, onGoogle }: SocialButtonsProps) {
  const socials = [
    { Icon: FacebookIcon, label: "Facebook", onClick: onFacebook },
    { Icon: AppleIcon,   label: "Apple",    onClick: onApple },
    { Icon: GoogleIcon,  label: "Google",   onClick: onGoogle },
  ];

  return (
    <div className="auth-social-row">
      {socials.map(({ Icon, label, onClick }) => (
        <button
          key={label}
          type="button"
          className="auth-social-btn"
          aria-label={`Continue with ${label}`}
          onClick={onClick}
        >
          <Icon />
        </button>
      ))}
    </div>
  );
}
